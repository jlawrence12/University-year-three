

/** Class representing a member of staff */
public class Staff {
    //Id of staff member
    String id;
    
    //First name
    String firstName;
    
    //Last name
    String surname;

    
    //Getters and setters
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getFirstName() {
        return firstName;
    }
    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }
    public String getSurname() {
        return surname;
    }
    public void setSurname(String surname) {
        this.surname = surname;
    }
}

